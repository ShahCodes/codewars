// Question:

/* 
Given an array of integers, return a new array with each value doubled.

For example:

[1, 2, 3] --> [2, 4, 6]
*/

// Solution: 


function maps(x){
  return x.map(n => n * 2)
}

maps([1,2,3,4,5,6,7,8,9,])